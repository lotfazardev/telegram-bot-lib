/**
 * Represents the bot's description.
 *
 * This object contains the description of the bot, which is typically displayed to users
 * when they interact with the bot, explaining its functionality or purpose.
 *
 */
export interface BotDescription {
  /**
   * The description of the bot.
   *
   * This field provides a brief explanation of what the bot does or its purpose.
   *
   * @example "A bot that helps you manage your tasks and reminders."
   */
  description: string
}
